<template>
<div class="tab-control">
    <div class="tab-control-item" v-for="(item,index) in titles" :key="index" :class="{active:index===curIndex}" @click='itemClick(index)'>
        <span>
            {{item}}
        </span>
    </div>
</div>
</template>

<script>
export default {
    props: {
        titles: {
            type: Array,
            default () {
                return []

            }
        }
    },
    data() {
        return {
            curIndex: 0
        }

    },
    methods: {
        itemClick(index) {
            this.curIndex = index
            this.$emit('tabClick', index)

        }
    }

}
</script>

